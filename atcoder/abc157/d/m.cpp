/* pair operator */
template <class X>
pair<X, X> operator+(pair<X, X> &ob1, pair<X, X> &ob2)
{
  pair<X, X> res;
  res.first = ob1.first + ob2.first;
  res.second = ob1.second + ob2.second;
  return res;
}

template <class X>
pair<X, X> operator-(pair<X, X> &ob1, pair<X, X> &ob2)
{
  pair<X, X> res;
  res.first = ob1.first - ob2.first;
  res.second = ob1.second - ob2.second;
  return res;
}

template <class X>
pair<X, X> operator*(pair<X, X> &ob1, pair<X, X> &ob2)
{
  pair<X, X> res;
  res.first = ob1.first * ob2.first;
  res.second = ob1.second * ob2.second;
  return res;
}

template <class X>
pair<X, X> operator/(pair<X, X> &ob1, pair<X, X> &ob2)
{
  pair<X, X> res;
  res.first = ob1.first / ob2.first;
  res.second = ob1.second / ob2.second;
  return res;
}

int main()
{
  int n, m, k;
  cin >> n >> m >> k;

  VV fe(n);
  V be(n);

  dsu re(n);

  REP(i, m)
  {
    int a, b;
    cin >> a >> b;
    fe[a - 1].push_back(b - 1);
    fe[b - 1].push_back(a - 1);
    if (re.same(a - 1, b - 1) == false)
    {
      re.merge(a - 1, b - 1);
    }
  }

  REP(i, k)
  {
    int a, b;
    cin >> a >> b;
    if (re.same(a - 1, b - 1) == true)
    {
      be[a - 1]++;
      be[b - 1]++;
    }
  }

  REP(i, n)
  {
    int ans = re.size(i) - (fe[i].size() + be[i]) - 1;
    cout << ans << " ";
  }
  cout << endl;

  return 0;
}